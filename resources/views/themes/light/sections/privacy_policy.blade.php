la configuración de todas o algunas cookies. Consulte el área de ayuda y
soporte técnico de su navegador de Internet para obtener instrucciones
sobre cómo bloquear o eliminar las cookies. Sin embargo, si utiliza la
configuración de su navegador para bloquear todas las cookies, incluidas
las cookies esenciales, es posible que no pueda acceder a la totalidad o a
algunas partes de nuestro sitio.
Algunas cosas importantes que debe saber sobre las cookies son las
siguientes:
a. Ofrecemos ciertas características que sólo están disponibles mediante
el uso de cookies.
b. Utilizamos cookies para ayudar a identificarlo y mantener su estado de
inicio de sesión.
c. La mayoría de las cookies son "cookies de sesión", lo que significa
que se eliminan automáticamente de su disco duro al final de una
sesión.
d. Usted siempre es libre de rechazar nuestras cookies, aunque hacerlo
puede interferir con el uso de algunos puntos de nuestro Sitio o
servicios.
e. Es posible que encuentre cookies de terceros en ciertas páginas del
Sitio que no controlamos. Por ejemplo, si usted ve una página web
creada por otro usuario, puede que haya una cookie colocada por esa
página web.
f. Tenga en cuenta que los terceros, incluyendo, por ejemplo, las redes
de publicidad y los proveedores de servicios externos como los
servicios de análisis de tráfico web, también pueden utilizar cookies,
sobre las que no tenemos ningún control.
g. Nuestro sitio web usa cookies. Una cookie es un fichero que contiene
un identificador, una cadena de letras y números, que se envía desde
un servidor web a un navegador web y se almacena en el navegador.
El identificador es enviado de vuelta al servidor cada vez que el
navegador solicita una página del servidor. Las cookies pueden ser
“persistentes” o de “sesión”. Una cookie persistente será almacenada
por un navegador web y permanecerá válida hasta su fecha de
caducidad, a menos que el usuario la elimine antes de la fecha de
caducidad. Una cookie de sesión caducará al final de la sesión del
usuario, cuando se cierre el navegador. Las cookies típicamente no
contienen ninguna información que identifique personalmente al
usuario, sino información personal que almacenamos sobre usted que
puede relacionarse con la información almacenada.
